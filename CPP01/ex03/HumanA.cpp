void	HumanA::attack(void)
{
	std::string	type;

	type = _weapon.getType();
	if (!type.size())
		std::cout << getName()
				  << " attacks with bare hand, because doesn't have weapon"
				  << std::endl;
	else
		std::cout << getName() << " attacks with their " << type
				  << std::endl;
}

